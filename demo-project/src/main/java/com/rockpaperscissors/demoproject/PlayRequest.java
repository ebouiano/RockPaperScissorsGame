package com.rockpaperscissors.demoproject;


/**
 * A class representing a request to play a game.
 */
public class PlayRequest {
     /** The name of the player making the request. */
    private String player;

     /**
     * Returns the name of the player making the request.
     * @return the name of the player making the request
     */
    public String getPlayer() {
        return player;
    }
}


